public class Car {
    String brand;
    String color;
    int year;
    int price;
    int carID;

    public Car(String brand, String color, int year, int price, int carID) {
        this.brand = brand;
        this.color = color;
        this.year = year;
        this.price = price;
        this.carID = carID;
    }

}
